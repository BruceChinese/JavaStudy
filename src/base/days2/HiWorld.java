package base.days2;

public class HiWorld {
    public static void main(String[] args) {
        System.out.print("姓名: ");
        System.out.print("丽丽\n");
        System.out.print("职业: ");
        System.out.print("学生\n");


        System.out.println();
        System.out.println("1ddddddddddddddddd");
        System.out.println("2dddddddddddddddddddddd\t222222222222222");
    }
}


/*
1、System.out.print和System.out.println区别联系：
System.out.print：将双引号中内容鸳鸯输出，不换行
System.out.println：将双引号内容原样输出，换行
2、转义字符
\就是转义字符：作用：将后面普通的字母转换为特殊含义
\n: 换行
\t：距离前面有一个制表符位置
 */